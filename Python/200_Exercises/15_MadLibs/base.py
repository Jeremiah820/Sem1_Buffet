weird_noun = input("Weird noun: ")
verb_ing = input("Verb ending in -ing: ")
silly_adjective = input("Silly adjective: ")
food = input("Name of a food: ")
quantity = input("Quantity: ")
famous_person = input("Famous person: ")
funky_noise = input("Funky noise: ")


story = f"""
One day, I was {verb_ing} with a {silly_adjective} {weird_noun}.
We found {quantity} plates of {food} on the ground!
Then {famous_person} showed up and yelled, "{funky_noise}!"

The {weird_noun} got scared and started {verb_ing} even faster.
It was the most {silly_adjective} day ever!
"""

print(story)